package Dominio;

public class MatriculasModalidades {
	private int matriculas_codigo;
	private int modalidades_codigo;
	private int graduacoes_codigo;
	private int planos_codigo;
	
	public int getMatriculas_codigo() {
		return matriculas_codigo;
	}
	public void setMatriculas_codigo(int matriculas_codigo) {
		this.matriculas_codigo = matriculas_codigo;
	}
	public int getModalidades_codigo() {
		return modalidades_codigo;
	}
	public void setModalidades_codigo(int modalidades_codigo) {
		this.modalidades_codigo = modalidades_codigo;
	}
	public int getGraduacoes_codigo() {
		return graduacoes_codigo;
	}
	public void setGraduacoes_codigo(int graduacoes_codigo) {
		this.graduacoes_codigo = graduacoes_codigo;
	}
	public int getPlanos_codigo() {
		return planos_codigo;
	}
	public void setPlanos_codigo(int planos_codigo) {
		this.planos_codigo = planos_codigo;
	}
}
