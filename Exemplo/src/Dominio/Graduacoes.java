package Dominio;

public class Graduacoes {

	private int id;
	private String descricao;
	private int modalidades_codigo;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public int getModalidades_codigos() {
		return modalidades_codigo;
	}
	public void setModalidades_codigos(int modalidades_codigos) {
		this.modalidades_codigo = modalidades_codigos;
	}

	
}
